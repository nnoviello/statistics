package org.grayleaves.problem;

public class ResetStateForTestingException extends RuntimeException
{
	private static final long serialVersionUID = 1L;

	public ResetStateForTestingException()
	{
	}

	public ResetStateForTestingException(String arg0)
	{
		super(arg0);
	}

	public ResetStateForTestingException(Throwable arg0)
	{
		super(arg0);
	}

	public ResetStateForTestingException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}

}
